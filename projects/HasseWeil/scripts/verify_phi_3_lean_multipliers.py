print(f"Check (should be 0): {check}")

# Now we have:  residual = M_b * (bâ‚ˆ - bâ‚‚آ·bâ‚† + bâ‚„آ²) + 3 آ· M_3
# where M_3 = residual_div_3.
# To find M_b, compute (residual - 3 آ· M_3) / (bâ‚ˆ - bâ‚‚آ·bâ‚† + bâ‚„آ²).
# But we don't know M_3 directly â€” only after b-substitution.
# Strategy: residual_orig = residual itself. We know residual| bâ‚ˆâ†’bâ‚‚bâ‚†-bâ‚„آ² = 3آ·M_3.
# So residual - 3آ·M_3 = M_b آ· (bâ‚ˆ - bâ‚‚آ·bâ‚† + bâ‚„آ²).
# But M_3 has been computed AFTER bâ‚ˆ-substitution, so we need to find an M_3
# (possibly different) that works WITHOUT substitution. Actually, the cleanest
# linear_combination form treats both relations at once.

# Strategy 2: Use polynomial division.
# residual is a polynomial in (X, bâ‚‚, bâ‚„, bâ‚†, bâ‚ˆ). The element bâ‚ˆ - bâ‚‚آ·bâ‚† + bâ‚„آ²
# is linear in bâ‚ˆ. Divide by bâ‚ˆ - bâ‚‚آ·bâ‚† + bâ‚„آ² treating other vars as constants.

