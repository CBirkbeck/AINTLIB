# Witness g(X) = Xآ³ + 2آ·bâ‚‚آ·bâ‚„آ·Xآ² + (2آ·bâ‚‚آ³آ·bâ‚† + bâ‚‚آ²آ·bâ‚„آ² + bâ‚‚آ·bâ‚„آ·bâ‚†)آ·X +
#                (2آ·bâ‚‚آ·bâ‚„آ·bâ‚†آ² + bâ‚„آ³آ·bâ‚† + bâ‚†آ³)
g_coeff_X3 = 1
